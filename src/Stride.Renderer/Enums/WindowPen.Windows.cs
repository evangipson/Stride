namespace Stride.Renderer.Enums;

/// <summary>
/// A collection of potential window pens, to allow drawing color on a window.
/// </summary>
internal enum WindowPen
{
    /// <summary>
    /// Represents a white pen.
    /// </summary>
    White = 6,

    /// <summary>
    /// Represents a black pen.
    /// </summary>
    Black,

    /// <summary>
    /// Represents a transparent pen.
    /// </summary>
    Transparent
}
