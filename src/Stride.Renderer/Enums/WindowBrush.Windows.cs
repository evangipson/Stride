namespace Stride.Renderer.Enums;

/// <summary>
/// A collection of potential background brushes (colors) for a window.
/// </summary>
internal enum WindowBrush
{
    /// <summary>
    /// Represents a white background brush.
    /// </summary>
    White = 0,

    /// <summary>
    /// Represents a light gray background brush.
    /// </summary>
    LightGray,

    /// <summary>
    /// Represents a gray background brush.
    /// </summary>
    Gray,

    /// <summary>
    /// Represents a dark gray background brush.
    /// </summary>
    DarkGray,

    /// <summary>
    /// Represents a black background brush.
    /// </summary>
    Black,
    
    /// <summary>
    /// Represents a transparent background brush.
    /// </summary>
    Transparent
}
